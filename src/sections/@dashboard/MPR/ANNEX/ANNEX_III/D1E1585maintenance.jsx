export const D1E1624article = () => (
  <div>
    <p>
      Machinery or related products shall be fitted with means to isolate it from all energy sources. Such isolators
      shall be clearly identified. They shall be capable of being locked if reconnection could endanger persons.
      Isolators shall also be capable of being locked where an operator is unable, from any of the points to which he or
      she has access, to check that the energy is still cut off.
    </p>
    <p>
      In the case of machinery or related products capable of being plugged into an electricity supply, removal of the
      plug is sufficient, if the operator can check from any of the points to which he or she has access that the plug
      remains removed.
    </p>
    <p>
      After the energy is cut off, it shall be possible to dissipate normally any energy remaining or stored in the
      circuits of the machinery or related product without risk to persons.
    </p>
    <p>
      As an exception to the requirement laid down in the previous paragraphs, certain circuits may remain connected to
      their energy sources in order, for example, to hold parts, to protect information, to light interiors, etc. In
      this case, special steps shall be taken to ensure operator safety.
    </p>
  </div>
);

